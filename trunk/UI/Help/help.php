<p><a name="editR"><b><font size="4"><u>C. Rank </u></font></b></p>

<p>There are 4 different rank in ACLPS.</p>
<p>1. Anonymous - Can only view public posts.</p>
<p>2. Author - Can edit and delete&nbsp;his/her own posts and comments</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;in that blog but cannot edit and delete anyone else</p>
<p>3. Editor - Can edit and delete anybody's posts and comments </p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;in that blog.</p>
<p>4. Owner - Same <span lang=EN-US>privilege as editor but he/she owns the blog.</span></p>



